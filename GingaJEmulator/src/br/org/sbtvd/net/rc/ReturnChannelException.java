
package br.org.sbtvd.net.rc;


/**
  * Base class for the ReturnChannelExceptions
  */
public class ReturnChannelException extends java.lang.Exception {

  /** Default constructor for the exception
    */
  public ReturnChannelException() {}

  /** Constructor for the return channel exception with a specified reason
    * @param reason the reason why the exception was raised
    */
  public ReturnChannelException(String reason) {}


}
